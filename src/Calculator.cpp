#include "include/Calculator.h"

double Calculator::Sum(double x, double y)
{
    return x + y;
}

double Calculator::Sub(double x, double y)
{
    return x - y;
}

double Calculator::Multiply(double x, double y)
{
    return x * y;
}

double Calculator::Divide(double x, double y)
{
    return x / y;
}
